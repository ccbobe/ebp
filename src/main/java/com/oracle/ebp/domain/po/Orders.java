package com.oracle.ebp.domain.po;

import java.sql.Timestamp;

public class Orders {
	
	private int oid;	
	private  Timestamp  commitTime;
	private double amount;
	private int uid;
	private int status;
	
	public int getOid() {
		return oid;
	}
	
	public Timestamp getCommitTime() {
		return commitTime;
	}
	
	public double getAmount() {
		return amount;
	}
	
	public int getUid() {
		return uid;
	}
	
	public int getStatus() {
		return status;
	}
	
	public void setOid(int oid) {
		this.oid = oid;
	}
	
	public void setCommitTime(Timestamp commitTime) {
		this.commitTime = commitTime;
	}
	
	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	public void setUid(int uid) {
		this.uid = uid;
	}
	
	public void setStatus(int status) {
		this.status = status;
	}
	
	@Override
	public String toString() {
		return "Orders [oid=" + oid + ", commitTime=" + commitTime
				+ ", amount=" + amount + ", uid=" + uid + ", status=" + status
				+ "]";
	}

	public Orders(int oid, Timestamp commitTime, double amount, int uid,
			int status) {
		super();
		this.oid = oid;
		this.commitTime = commitTime;
		this.amount = amount;
		this.uid = uid;
		this.status = status;
	}

	public Orders() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	

}
